package net.mehrad.mapinfo2kml.mif;

/**
 * 
 * @author Mehrad
 *
 */
public class MifSymbol {

	private int shape;
	private int color;
	private int size;
	
	public int getShape() {
		return shape;
	}
	public void setShape(int shape) {
		this.shape = shape;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
}
