package net.mehrad.mapinfo2kml.xls;

public class FederalStateElectorateMapping {

	private String state;
	private String federalSeat;
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getFederalSeat() {
		return federalSeat;
	}
	public void setFederalSeat(String federalSeat) {
		this.federalSeat = federalSeat;
	}
	public String getStateSeat() {
		return stateSeat;
	}
	public void setStateSeat(String stateSeat) {
		this.stateSeat = stateSeat;
	}
	private String stateSeat;

}
